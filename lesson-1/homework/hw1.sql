 3. Когда подключаешься к Microsoft SQL Server, можно использовать несколько режимов аутентификации. Два основных: 
     * Режимы аутентификации SQL Server, Аутентификация Windows (Windows Authentication)
- Использует учетные данные операционной системы Windows.
- Удобна в корпоративной сети: пользователи автоматически проходят проверку без ввода имени и пароля.
- Безопаснее, так как применяются политики безопасности Windows (например, сложность пароля, срок действия).
     * Аутентификация SQL Server (SQL Server Authentication)
- Пользователь вводит имя и пароль, созданные в SQL Server.
- Не зависит от учетных записей Windows.
- Полезна при доступе из внешних приложений или в средах без Active Directory.

