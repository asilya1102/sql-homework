1. Данные - Совокупность цифр, фактов, символов, а также описаний, которые используются для анализа, обработки и дальнейшего принятия решений. Данные — это также информация в сыром виде, которую можно накапливать, хранить, а также анализировать.
База данных - своего рода коллекция, совокупность данных, хранящаяся и управляемая с помощью программного обеспечения, которое позволяет легко получать доступ, обновлять, а также защищать данные. Также это некая структура для хранения и управления данными, или цифровой архив.
Реляционная база данных - один из типов базы данных, в которой данные представлены в виде связанных таблиц, имеющих столбцы и строки,которые связываются между собой с помощью ключей. 
Таблица - своего рода сетка данных, содержащая в себе колонки и строки, в которыххранятся данные.
