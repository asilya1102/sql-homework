7. Data Definition Language (Язык определения данных). Предназначен для создания и изменения структуры базы данных (в т.ч. таблиц, схем, индексов). Используется для описания и изменения структуры базы данных.
CREATE TABLE customers (...) - создать таблицу
ALTER TABLE customers ADD COLUMN Age INT - Изменить таблицу
DROP TABLE customers - удалить таблицу
TRUNCATE TABLE customers - очистить таблицу

Data Manipulation Language (Язык манипуляции данными). Предназначен для работы с данными внутри таблиц — добавление, изменение, удаление.
INSERT INTO customers VALUES (1, 'Egor', 20) — вставить данные
UPDATE customers SET Age = 28 WHERE customerID = 1 — обновить данные
DELETE FROM customers WHERE Age < 19 — удалить данные

Data Query Language (Язык запросов данных). Предназначен для получения данных из базы.
SELECT Name, Age FROM customers WHERE Age > 18 — выбрать данные

Data Control Language (Язык управления доступом). Предназначен для управления правами доступа к данным, регулирует, кто может видеть или изменять данные.
GRANT SELECT ON customers TO user1 — предоставить доступ
REVOKE SELECT ON customers FROM user1 — отозвать доступ

Transaction Control Language (Язык управления транзакциями) для управления группой операций как одной транзакцией. Обеспечивает целостность данных при выполнении нескольких операций.
BEGIN TRANSACTION — начать транзакцию
COMMIT — зафиксировать изменения
ROLLBACK — отменить изменения
SAVEPOINT sp1 — создать точку отката.

