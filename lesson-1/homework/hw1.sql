6. SQL Server - система управления базами данных от Microsoft, которое обеспечивает хранение, обработку, а также защиту данных. Это система, позволяющая работать с базами данных.
   SSMS (SQL Server Management Studio) -программа с интерфейсом, предназначенная для управления SQL Server. Она позволяет пользователю запускать запросы, создавать базы, а также управлять ими.
   SQL (Structures Query Language) - язык запросов к базам данных. Позволяет извлекать, изменять, а также управлять даными. То есть SQL является языком, а SQL SERVER является движком, который понимает его. 

